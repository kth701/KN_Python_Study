# 형변환

# 실수 -> 정수
a =  int(10.2)
b = int(20.42)
add = a+ b
print(a, b, add)
print(type(a), type(b), type(add))

# 정수 -> 실수
a1 = float(10)
b1 = float(20)
add1 = a1 + b1
print(a1, b1, add1)
print(type(a1), type(b1), type(add1))

print(int(True), int(False))

st = '100'
print(int(st)**2, type(int(st)))

